int main(void) {
    // Poison the heap so uninitialized malloc returns familiar data
    void *trash = malloc(100000);
    memset(trash, 0xCD, 100000);
    free(trash);

    int x, y, comp;
    unsigned char *img = stbi_load_from_memory(prog_jpeg, sizeof(prog_jpeg), &x, &y, &comp, 0);
    if (!img) {
        printf("PASS: Image rejected\n");
        return 0;
    }

    int leaked = 0;
    for (int i = 0; i < x * y * comp; i++) {
        if (img[i] == 0xCD) {
            leaked++;
        }
    }

    stbi_image_free(img);
    if (leaked) {
        printf("FAIL: Found %d pixels with leaked 0xCD (uninitialized coeff data)\n", leaked);
        return 1;
    }
    printf("PASS: No leaked heap data in output\n");
    return 0;
}
