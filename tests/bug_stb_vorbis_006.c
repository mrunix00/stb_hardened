    size_t buf_size = 1024;
    void *alloc_buf = malloc(buf_size);
    if (!alloc_buf) { printf("FAIL: malloc\n"); return 1; }
    stb_vorbis_alloc alloc;
    alloc.alloc_buffer = (char *)alloc_buf;
    alloc.alloc_buffer_length_in_bytes = (int)buf_size;

