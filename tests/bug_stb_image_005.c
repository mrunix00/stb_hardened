#define STB_IMAGE_IMPLEMENTATION
#define STBI_NO_STDIO
#include "stb_image.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main() {
    unsigned char data[] = {
        0x47,0x49,0x46,0x38,0x39,0x61,0xbd,0x00,0xdf,0x79,0xa9,0x97,0x53,
        0x43,0x05,0xff,0xbe,0x21,0x00,0x30,0x03,0x01,0x00,0x21,0x00,0x2c,
        0x00,0x00,0x00,0x00,0xbd,0x00,0x3f,0x71,0x07,0x00,0x05,0xff,0xbe,
        0x01,0x00,0x68,0x00,0x21,0xf9,0x04,0x2c,0x0a,0x00,0x1f,0x00,0x2c,
        0x00,0x00,0x00,0x00,0xbd,0x00,0x71,0x00,0x00,0x05,0xff,0xe0,0x27,
        0x8e,0x64,0x68
    };
    size_t size = sizeof(data);

    printf("BUG-stb_image-005: Verifying two_back fix in GIF dispose-3...\n");

    int x, y, z, channels;
    unsigned char *img = stbi_load_gif_from_memory(data, (int)size, NULL, &x, &y, &z, &channels, 4);
    if (!img) {
        printf("FAIL: stbi_load_gif_from_memory returned NULL: %s\n", stbi_failure_reason());
        return 1;
    }

    printf("OK: loaded %d frames (%dx%d, %d channels), img=%p\n", z, x, y, channels, img);
    stbi_image_free(img);
    return 0;
}
